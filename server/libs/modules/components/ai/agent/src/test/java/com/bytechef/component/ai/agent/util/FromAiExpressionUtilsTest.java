package com.bytechef.component.ai.agent.util;

import static org.junit.jupiter.api.Assertions.assertEquals;

import java.util.List;
import org.junit.jupiter.api.Test;

/**
 * @author Ivica Cardic
 */
class FromAiExpressionUtilsTest {

    @Test
    void testExtractFromAiCallStringsSingleCall() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings("=fromAi('subject')");

        assertEquals(List.of("fromAi('subject')"), calls);
    }

    @Test
    void testExtractFromAiCallStringsSingleCallWithMultipleArgs() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings(
            "=fromAi('count', 'Number of items', 'INTEGER', 10)");

        assertEquals(List.of("fromAi('count', 'Number of items', 'INTEGER', 10)"), calls);
    }

    @Test
    void testExtractFromAiCallStringsMultipleCalls() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings(
            "='Generated by AI: ' + fromAi('subject') + ' by ' + fromAi('name')");

        assertEquals(List.of("fromAi('subject')", "fromAi('name')"), calls);
    }

    @Test
    void testExtractFromAiCallStringsThreeCalls() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings(
            "=fromAi('greeting') + ' ' + fromAi('name') + ', ' + fromAi('message')");

        assertEquals(List.of("fromAi('greeting')", "fromAi('name')", "fromAi('message')"), calls);
    }

    @Test
    void testExtractFromAiCallStringsNoCallsPlainText() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings("='plain text'");

        assertEquals(List.of(), calls);
    }

    @Test
    void testExtractFromAiCallStringsNoCallsEmptyString() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings("");

        assertEquals(List.of(), calls);
    }

    @Test
    void testExtractFromAiCallStringsWithEscapedQuotes() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings("=fromAi('it''s a name')");

        assertEquals(List.of("fromAi('it''s a name')"), calls);
    }

    @Test
    void testExtractFromAiCallStringsWithoutEqualsPrefix() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings("fromAi('subject')");

        assertEquals(List.of("fromAi('subject')"), calls);
    }

    @Test
    void testExtractFromAiCallStringsMixedWithArithmetic() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings(
            "=fromAi('count', null, 'INTEGER') + 10");

        assertEquals(List.of("fromAi('count', null, 'INTEGER')"), calls);
    }

    @Test
    void testExtractFromAiCallStringsWithNullArgument() {
        List<String> calls = FromAiExpressionUtils.extractFromAiCallStrings(
            "=fromAi('name', null, 'STRING', 'default')");

        assertEquals(List.of("fromAi('name', null, 'STRING', 'default')"), calls);
    }

    @Test
    void testToSpelLiteralNull() {
        assertEquals("null", FromAiExpressionUtils.toSpelLiteral(null));
    }

    @Test
    void testToSpelLiteralString() {
        assertEquals("'hello'", FromAiExpressionUtils.toSpelLiteral("hello"));
    }

    @Test
    void testToSpelLiteralStringWithQuotes() {
        assertEquals("'it''s fine'", FromAiExpressionUtils.toSpelLiteral("it's fine"));
    }

    @Test
    void testToSpelLiteralEmptyString() {
        assertEquals("''", FromAiExpressionUtils.toSpelLiteral(""));
    }

    @Test
    void testToSpelLiteralInteger() {
        assertEquals("42", FromAiExpressionUtils.toSpelLiteral(42));
    }

    @Test
    void testToSpelLiteralLong() {
        assertEquals("100", FromAiExpressionUtils.toSpelLiteral(100L));
    }

    @Test
    void testToSpelLiteralDouble() {
        assertEquals("3.14", FromAiExpressionUtils.toSpelLiteral(3.14));
    }

    @Test
    void testToSpelLiteralFloat() {
        assertEquals("2.5", FromAiExpressionUtils.toSpelLiteral(2.5f));
    }

    @Test
    void testToSpelLiteralBooleanTrue() {
        assertEquals("true", FromAiExpressionUtils.toSpelLiteral(true));
    }

    @Test
    void testToSpelLiteralBooleanFalse() {
        assertEquals("false", FromAiExpressionUtils.toSpelLiteral(false));
    }

}
