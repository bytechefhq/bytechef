package com.bytechef.component.ai.agent.action;

import static org.junit.jupiter.api.Assertions.assertDoesNotThrow;
import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertNull;
import static org.mockito.ArgumentMatchers.any;
import static org.mockito.ArgumentMatchers.anyBoolean;
import static org.mockito.ArgumentMatchers.eq;
import static org.mockito.Mockito.mock;
import static org.mockito.Mockito.mockStatic;
import static org.mockito.Mockito.when;

import com.bytechef.component.ai.llm.util.ModelUtils;
import com.bytechef.component.definition.ActionContext;
import com.bytechef.component.definition.Parameters;
import com.bytechef.component.test.definition.MockParametersFactory;
import com.bytechef.evaluator.Evaluator;
import com.bytechef.platform.component.ComponentConnection;
import com.bytechef.platform.component.definition.ai.agent.ModelFunction;
import com.bytechef.platform.component.service.ClusterElementDefinitionService;
import com.bytechef.platform.workflow.worker.ai.FromAiResult;
import java.util.HashMap;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.Mock;
import org.mockito.MockedStatic;
import org.mockito.junit.jupiter.MockitoExtension;
import org.springframework.ai.chat.model.ChatModel;

/**
 * @author Ivica Cardic
 */
@ExtendWith(MockitoExtension.class)
class AbstractAiAgentChatActionTest {

    @Mock
    private ClusterElementDefinitionService clusterElementDefinitionService;

    @Mock
    private Evaluator evaluator;

    @Test
    void testExtractFromAiResultsWithSingleFromAiExpression() {
        when(evaluator.evaluate(eq(Map.of("value", "=fromAi('subject')")), eq(Map.of())))
            .thenReturn(Map.of("value", new FromAiResult("subject", null, "STRING", null)));

        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        List<FromAiResult> results = action.extractFromAiResults(Map.of("title", "=fromAi('subject')"));

        assertEquals(1, results.size());
        assertEquals("subject", results.getFirst()
            .name());
    }

    @Test
    void testExtractFromAiResultsWithMultipleFromAiCallsInOneExpression() {
        when(evaluator.evaluate(eq(Map.of("value", "=fromAi('subject')")), eq(Map.of())))
            .thenReturn(Map.of("value", new FromAiResult("subject", null, "STRING", null)));
        when(evaluator.evaluate(eq(Map.of("value", "=fromAi('name')")), eq(Map.of())))
            .thenReturn(Map.of("value", new FromAiResult("name", null, "STRING", null)));

        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        List<FromAiResult> results = action.extractFromAiResults(
            Map.of("title", "='Generated by AI: ' + fromAi('subject') + ' by ' + fromAi('name')"));

        assertEquals(2, results.size());
        assertEquals("subject", results.get(0)
            .name());
        assertEquals("name", results.get(1)
            .name());
    }

    @Test
    void testExtractFromAiResultsDeduplicatesByName() {
        when(evaluator.evaluate(eq(Map.of("value", "=fromAi('subject')")), eq(Map.of())))
            .thenReturn(Map.of("value", new FromAiResult("subject", null, "STRING", null)));

        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        Map<String, Object> parameters = new LinkedHashMap<>();

        parameters.put("title", "='Title: ' + fromAi('subject')");
        parameters.put("body", "=fromAi('subject') + ' details'");

        List<FromAiResult> results = action.extractFromAiResults(parameters);

        assertEquals(1, results.size());
        assertEquals("subject", results.getFirst()
            .name());
    }

    @Test
    void testExtractFromAiResultsWithFromAiResultObjectValue() {
        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        FromAiResult directResult = new FromAiResult("directParam", "A direct param", "STRING", null);

        List<FromAiResult> results = action.extractFromAiResults(Map.of("param", directResult));

        assertEquals(1, results.size());
        assertEquals("directParam", results.getFirst()
            .name());
    }

    @Test
    void testExtractFromAiResultsWithNonFromAiParameters() {
        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        List<FromAiResult> results = action.extractFromAiResults(
            Map.of("title", "plain text", "count", 42));

        assertEquals(0, results.size());
    }

    @Test
    void testExtractFromAiResultsWithNullParameters() {
        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        List<FromAiResult> results = action.extractFromAiResults(null);

        assertEquals(0, results.size());
    }

    @Test
    void testResolveParameterValueWithFromAiResultObject() {
        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        FromAiResult fromAiResult = new FromAiResult("subject", null, "STRING", null);

        Object result = action.resolveParameterValue(fromAiResult, Map.of("subject", "Hello World"));

        assertEquals("Hello World", result);
    }

    @Test
    void testResolveParameterValueWithFromAiResultObjectFallsBackToDefault() {
        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        FromAiResult fromAiResult = new FromAiResult("subject", null, "STRING", "default value");

        Object result = action.resolveParameterValue(fromAiResult, Map.of());

        assertEquals("default value", result);
    }

    @Test
    void testResolveParameterValueWithFromAiResultObjectReturnsNullWhenNoDefaultAndNotInRequest() {
        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        FromAiResult fromAiResult = new FromAiResult("subject", null, "STRING", null);

        Object result = action.resolveParameterValue(fromAiResult, Map.of());

        assertNull(result);
    }

    @Test
    void testResolveParameterValueWithPureFromAiExpression() {
        when(evaluator.evaluate(eq(Map.of("value", "=fromAi('subject')")), eq(Map.of())))
            .thenReturn(Map.of("value", new FromAiResult("subject", null, "STRING", null)));

        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        Object result = action.resolveParameterValue("=fromAi('subject')", Map.of("subject", "Hello World"));

        assertEquals("Hello World", result);
    }

    @Test
    void testResolveParameterValueWithPureFromAiExpressionPreservesIntegerType() {
        when(evaluator.evaluate(eq(Map.of("value", "=fromAi('count', null, 'INTEGER')")), eq(Map.of())))
            .thenReturn(Map.of("value", new FromAiResult("count", null, "INTEGER", null)));

        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        Object result = action.resolveParameterValue(
            "=fromAi('count', null, 'INTEGER')", Map.of("count", 42));

        assertEquals(42, result);
    }

    @Test
    void testResolveParameterValueWithCompositeExpression() {
        when(evaluator.evaluate(eq(Map.of("value", "=fromAi('subject')")), eq(Map.of())))
            .thenReturn(Map.of("value", new FromAiResult("subject", null, "STRING", null)));
        when(evaluator.evaluate(eq(Map.of("value", "=fromAi('name')")), eq(Map.of())))
            .thenReturn(Map.of("value", new FromAiResult("name", null, "STRING", null)));
        when(evaluator.evaluate(
            eq(Map.of("value", "='Generated by AI: ' + 'Hello World' + ' by ' + 'John'")), eq(Map.of())))
                .thenReturn(Map.of("value", "Generated by AI: Hello World by John"));

        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        Object result = action.resolveParameterValue(
            "='Generated by AI: ' + fromAi('subject') + ' by ' + fromAi('name')",
            Map.of("subject", "Hello World", "name", "John"));

        assertEquals("Generated by AI: Hello World by John", result);
    }

    @Test
    void testResolveParameterValueWithCompositeExpressionUsesDefaults() {
        when(evaluator.evaluate(eq(Map.of("value", "=fromAi('subject')")), eq(Map.of())))
            .thenReturn(Map.of("value", new FromAiResult("subject", null, "STRING", "Default Subject")));
        when(evaluator.evaluate(eq(Map.of("value", "='Title: ' + 'Default Subject'")), eq(Map.of())))
            .thenReturn(Map.of("value", "Title: Default Subject"));

        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        Object result = action.resolveParameterValue("='Title: ' + fromAi('subject')", Map.of());

        assertEquals("Title: Default Subject", result);
    }

    @Test
    void testResolveParameterValueWithNonFromAiStringValue() {
        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        Object result = action.resolveParameterValue("plain text", Map.of());

        assertEquals("plain text", result);
    }

    @Test
    void testResolveParameterValueWithNonStringValue() {
        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        Object result = action.resolveParameterValue(42, Map.of());

        assertEquals(42, result);
    }

    @Test
    void testGetChatClientRequestSpecWithNullParameterValues() throws Exception {
        HashMap<String, Object> inputParamsMap = new HashMap<>();

        inputParamsMap.put("key1", "value1");
        inputParamsMap.put("nullableKey", null);

        Parameters inputParameters = MockParametersFactory.create(inputParamsMap);

        HashMap<String, Object> clusterElementParams = new HashMap<>();

        clusterElementParams.put("model", "gpt-4o");
        clusterElementParams.put("nullableParam", null);

        Map<String, Object> modelElement = new HashMap<>();

        modelElement.put("name", "model_1");
        modelElement.put("type", "testComponent/v1/testModel");
        modelElement.put("parameters", clusterElementParams);

        Parameters extensions = MockParametersFactory.create(
            Map.of("clusterElements", Map.of("model", modelElement)));

        ModelFunction modelFunction = mock(ModelFunction.class);

        ChatModel chatModel = mock(ChatModel.class);

        when(modelFunction.apply(any(), any(), anyBoolean())).thenAnswer(invocation -> chatModel);
        when(clusterElementDefinitionService.<ModelFunction>getClusterElement(
            eq("testComponent"), eq(1), eq("testModel"))).thenReturn(modelFunction);

        ComponentConnection componentConnection = new ComponentConnection(
            "testComponent", 1, 1L, Map.of(), null);

        Map<String, ComponentConnection> connectionParameters = Map.of("model_1", componentConnection);

        ActionContext actionContext = mock(ActionContext.class);

        TestAiAgentChatAction action = new TestAiAgentChatAction(clusterElementDefinitionService, evaluator);

        try (MockedStatic<ModelUtils> modelUtilsMockedStatic = mockStatic(ModelUtils.class)) {
            modelUtilsMockedStatic.when(() -> ModelUtils.getMessages(any(), any()))
                .thenReturn(List.of());

            assertDoesNotThrow(() -> action.getChatClientRequestSpec(
                inputParameters, connectionParameters, extensions, null, actionContext));
        }
    }

    private static class TestAiAgentChatAction extends AbstractAiAgentChatAction {

        TestAiAgentChatAction(ClusterElementDefinitionService clusterElementDefinitionService, Evaluator evaluator) {
            super(clusterElementDefinitionService, evaluator);
        }
    }

}
