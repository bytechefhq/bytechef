package com.bytechef.platform.configuration.web.rest;

import com.bytechef.atlas.coordinator.annotation.ConditionalOnCoordinator;
import com.bytechef.platform.configuration.web.rest.schema.SchemaGenerator;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

/**
 * @author Ivica Cardic
 */
@RestController
@RequestMapping("${openapi.openAPIDefinition.base-path.platform:}/internal")
@ConditionalOnCoordinator
public class WorkflowSchemaGeneratorApiController {

    private static final Logger logger = LoggerFactory.getLogger(WorkflowSchemaGeneratorApiController.class);

    private final SchemaGenerator schemaGenerator;

    public WorkflowSchemaGeneratorApiController(SchemaGenerator schemaGenerator) {
        this.schemaGenerator = schemaGenerator;
    }

    @PostMapping("/generate-schema")
    public String generateSchema(@RequestBody String json) {
        return schemaGenerator.generateSchemaFromJson(json);
    }
}
